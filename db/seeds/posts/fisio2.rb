<p>Es una de las lesiones más incapacitantes y uno de los motivos que más visitas produce en los centros clínicos. Dadas las condiciones de trabajo y vida, son numerosas las personas que padecen, o han padecido, lesión o dolor en la columna cervical.</p>
<p><img alt="Cervicalgia cuello, Barrio de la Concepcion, 28027 madrid" src="http://res.cloudinary.com/confisio/image/upload/v1455061873/8105284795_a02aff797a_o_kxrohi_bnr0vt.jpg"></p>
<p><strong>Cervicalgia aguda</strong>&nbsp;(tortícolis): aparece de forma repentina, dolor incapacitante que limita la movilidad cervical, y por lo tanto la mayoría de las actividades de la vida diaria, como por ejemplo conducir, o estar sentado mirando a una pantalla.</p>
<p>Es usual que el dolor aumente progresivamente en el tiempo tras el acontecimiento lesivo.</p>
<p>Tras la primera sesión de fisioterapia es habitual que mejore la movilidad cervical, y percepción del dolor por parte del paciente, capacitándolo para retomar varias de sus actividades diarias.</p>
<p><strong>Cervicalgia crónica:</strong> Es una entidad caracterizada por disturbios mas allá de la zona lesiva, ya que existen factores que mantienen la percepción de dolor del paciente, por ello hay que realizar un minucioso diagnostico clínico para averiguar los factores que impiden la resolución del dolor, y otros síntomas asociados como:</p>
<p>Déficit de la movilidad</p>
<p>Cefaleas</p>
<p>Vértigos, mareos</p>
<p>Alteración del control motor muscular voluntario</p>
<p>Dolor irradiado hacia otros segmentos ( Hombros, brazos, tronco)</p>
<p>&nbsp;</p>
<p>International Classification of Functioning, Disability, and Health (ICF).</p>
